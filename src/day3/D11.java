package day3;

public class D11 {
    public static void main(String[] args) {
        int a = 2;
        int b = 3 ;
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(~b);
        System.out.println(b>>a);
        System.out.println(b<<a);
    }
}
